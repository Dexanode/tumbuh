def build_system_prompt(profil_user: dict, history: list, knowledge_base_update: str = "") -> str:
    kb_section = KNOWLEDGE_BASE
    if knowledge_base_update:
        kb_section += f"\n\n[ UPDATE TERBARU ]\n{knowledge_base_update}"

    profil_text = f"""
PROFIL USER:
- Jenis usaha : {profil_user.get('kategori', '-')}
- Lama usaha  : {profil_user.get('lama_usaha', '-')}
- Tantangan   : {profil_user.get('tantangan', '-')}
- Omzet/bulan : {profil_user.get('omzet', '-')}
- Nama        : {profil_user.get('nama', 'belum diketahui')}
"""

    history_text = "RIWAYAT KONSULTASI:\n"
    if history:
        for i, s in enumerate(history[-3:], 1):
            history_text += f"\nSesi {i} ({s.get('tanggal','-')}): {s.get('topik','-')} | Action: {s.get('action_item','-')} | Status: {s.get('status','-')}\n"
    else:
        history_text += "Sesi pertama.\n"

    return "\n\n".join([IDENTITY, EXPERTISE, BEHAVIOR, FORMAT, FRAMEWORKS, kb_section, profil_text, history_text])
